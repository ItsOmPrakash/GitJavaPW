public class Relational_Operator {
    public static void main (String []args)
    {
    int a,b;
    a = 20;
    b = 10; 
    System.out.println(a == b);  //Equality Operator(==)
    System.out.println(a != b);  //Non Eqality Operator(!=)
    System.out.println(a < b);   //Less Then Operator(<)
    System.out.println(a > b);   //Grater Then Operator(>)
    System.out.println(a <= b);  //Less Then Equal To Operatoe(<=)
    System.out.println(a >= b);  // Grater Then Equal TO Operator(>=)

    }
}
